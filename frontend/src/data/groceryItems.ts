// Category mapping to match user's categories
export const CATEGORY_MAP: Record<string, string[]> = {
  dairy: ['dairy'],
  meat: ['meat'],
  poultry: ['meat'], // Poultry items (chicken, turkey) are in meat category
  seafood: ['seafood'],
  vegetables: ['vegetables'],
  fruits: ['fruits'],
  grains: ['pantry'], // Grains are in pantry (rice, quinoa, oats, etc.)
  bakery: ['pantry'], // Bakery items are in pantry
  beverages: ['pantry'], // Beverages are in pantry
  snacks: ['pantry'], // Snacks are in pantry
  frozen: ['dairy', 'meat', 'seafood', 'vegetables', 'fruits'], // Frozen items across categories
  canned: ['pantry', 'vegetables', 'fruits'], // Canned items
  condiments: ['pantry'], // Condiments are in pantry
  spices: ['pantry'], // Spices are in pantry
  'health & wellness': ['pantry'], // Health items are in pantry
  'cleaning supplies': ['pantry'], // Cleaning items are in pantry
}

// Helper to filter items by specific keywords for certain categories
const filterByKeywords = (items: string[], keywords: string[]): string[] => {
  return items.filter(item => 
    keywords.some(keyword => item.toLowerCase().includes(keyword.toLowerCase()))
  )
}

// Helper function to filter out items with units (size/weight/count)
const hasUnit = (item: string): boolean => {
  const unitPatterns = [
    /\d+\s*(oz|lbs?|lb|count|qt|gal|liter|L|ml|kg|g|pack|pcs?|pieces?)/i,
    /\d+\/\d+\s*(oz|lbs?|lb)/i, // e.g., "1/2 gal"
    /\d+-\d+\s*(oz|lbs?|lb|count)/i, // e.g., "12-16 oz"
    /^(8|12|16|24|32|48|64)\s*(oz|lbs?|lb|count)/i, // Common sizes at start
  ]
  return unitPatterns.some(pattern => pattern.test(item))
}

// Helper function to get items for a specific category (excluding items with units)
export const getGroceryItemsByCategory = (category: string): string[] => {
  const categoryLower = category.toLowerCase().trim()
  const sourceCategories = CATEGORY_MAP[categoryLower] || []
  if (sourceCategories.length === 0) return []
  
  let items: string[] = []
  sourceCategories.forEach(sourceCat => {
    if (GROCERY_ITEMS[sourceCat as keyof typeof GROCERY_ITEMS]) {
      items.push(...GROCERY_ITEMS[sourceCat as keyof typeof GROCERY_ITEMS])
    }
  })
  
  // Special filtering for specific categories
  if (categoryLower === 'poultry') {
    items = filterByKeywords(items, ['chicken', 'turkey'])
  } else if (categoryLower === 'grains') {
    items = filterByKeywords(items, ['rice', 'quinoa', 'oats', 'barley', 'wheat', 'bulgur', 'farro', 'millet', 'amaranth', 'freekeh', 'spelt', 'rye', 'cornmeal', 'polenta'])
  } else if (categoryLower === 'bakery') {
    items = filterByKeywords(items, ['bread', 'bagel', 'muffin', 'croissant', 'tortilla', 'bun', 'roll', 'crust', 'dough', 'biscuit', 'scone'])
  } else if (categoryLower === 'beverages') {
    items = filterByKeywords(items, ['water', 'juice', 'soda', 'coffee', 'tea', 'beer', 'wine', 'milk', 'drink', 'smoothie', 'energy'])
  } else if (categoryLower === 'snacks') {
    items = filterByKeywords(items, ['chip', 'cracker', 'cookie', 'pretzel', 'nut', 'popcorn', 'candy', 'chocolate', 'trail mix', 'granola bar'])
  } else if (categoryLower === 'condiments') {
    items = filterByKeywords(items, ['ketchup', 'mustard', 'mayo', 'sauce', 'dressing', 'salsa', 'relish', 'chutney', 'bbq', 'hot sauce', 'soy sauce', 'worcestershire'])
  } else if (categoryLower === 'spices') {
    items = filterByKeywords(items, ['salt', 'pepper', 'paprika', 'cumin', 'cinnamon', 'garlic powder', 'onion powder', 'oregano', 'basil', 'thyme', 'rosemary', 'sage', 'parsley', 'cilantro', 'spice', 'seasoning', 'herb'])
  } else if (categoryLower === 'health & wellness') {
    items = filterByKeywords(items, ['shampoo', 'conditioner', 'soap', 'toothpaste', 'toothbrush', 'deodorant', 'lotion', 'sunscreen', 'vitamin', 'supplement'])
  } else if (categoryLower === 'cleaning supplies') {
    items = filterByKeywords(items, ['detergent', 'soap', 'cleaner', 'bleach', 'wipe', 'sponge', 'trash bag', 'paper towel', 'tissue', 'napkin', 'foil', 'wrap', 'bag'])
  }
  
  // Filter items with units and remove duplicates
  return [...new Set(items.filter(item => !hasUnit(item)))]
}

// Helper function to get all items as a flat array (excluding items with units)
export const getAllGroceryItems = (): string[] => {
  return Object.values(GROCERY_ITEMS)
    .flat()
    .filter(item => !hasUnit(item))
}

